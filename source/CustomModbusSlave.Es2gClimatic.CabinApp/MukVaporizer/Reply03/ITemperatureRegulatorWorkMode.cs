namespace CustomModbusSlave.Es2gClimatic.CabinApp.MukVaporizer.Reply03
{
	/// <summary>
	/// Режим работы регулятора температуры
	/// </summary>
	interface ITemperatureRegulatorWorkMode {
		/// <summary>
		/// Полное значение (пара байт)
		/// </summary>
		int FullValue { get; }
		/// <summary>
		/// режим «охлаждение», иначе режим «нагрев»
		/// </summary>
		bool Cool { get; }
		/// <summary>
		/// режим ограничения охлаждения/нагрева по внешней температуре
		/// </summary>
		bool Restrict { get; }
	}
}